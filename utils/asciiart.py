def get_art(name: str) -> Optional[str]:
    if not name:
        return None
    return ART.get(name.lower())


def list_arts() -> Iterable[str]:
    return sorted(ART.keys())


def print_art(name: str, title: Optional[str] = None, border_style: str = "purple4", style: str = "white") -> None:
    art = get_art(name)
    if art is None:
        console.print(f"[red]ASCII art '{name}' introuvable. Cl√©s disponibles: {', '.join(list_arts())}[/red]")
        return

    panel = Panel(Align.center(art), title=title or name.capitalize(), border_style=border_style)
    console.print(panel)


